export default async function handler(req, res) {
  console.log('üîç [SETTINGS] Requisi√ß√£o recebida:', req.method, req.url)
  console.log('üîç [SETTINGS] Headers recebidos:', Object.keys(req.headers))
  console.log('üîç [SETTINGS] Authorization header:', req.headers['authorization'])
  
  // Configurar CORS
  res.setHeader('Access-Control-Allow-Origin', '*')
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, PUT, DELETE, OPTIONS')
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization')
  
  // Handle preflight requests
  if (req.method === 'OPTIONS') {
    console.log('üîç [SETTINGS] Preflight request - retornando 200')
    res.status(200).end()
    return
  }

  const authHeader = req.headers['authorization']
  const apiKey = authHeader ? authHeader.replace('Bearer ', '') : null
  
  console.log('üîç [SETTINGS] API Key extra√≠da:', apiKey ? 'SIM' : 'N√ÉO')
  
  if (!apiKey) {
    console.log('‚ùå [SETTINGS] API Key n√£o fornecida')
    return res.status(401).json({ error: 'API Key required' })
  }



  try {
    console.log('üîç [SETTINGS] Testando API Key com m√∫ltiplos endpoints...')
    console.log('üîç [SETTINGS] API Key sendo testada:', apiKey)
    
    // Testar m√∫ltiplos endpoints para encontrar um que funcione
    // Endpoints ordenados do mais b√°sico ao mais avan√ßado
    const endpoints = [
      'https://api.redtrack.io/report?group_by=campaign&date_from=2024-01-01&date_to=2024-12-31',
      'https://api.redtrack.io/conversions?limit=1',
      'https://api.redtrack.io/campaigns?limit=1',
      'https://api.redtrack.io/me/settings'
    ]
    
    let workingEndpoint = null
    let response = null
    
    for (const endpoint of endpoints) {
      console.log(`üîç [SETTINGS] Testando endpoint: ${endpoint}`)
      
      try {
        response = await fetch(endpoint, {
          method: 'GET',
          headers: {
            'Authorization': `Bearer ${apiKey}`,
            'Content-Type': 'application/json',
            'Accept': 'application/json',
            'User-Agent': 'TrackView-Dashboard/1.0'
          }
        })
        
        console.log(`üîç [SETTINGS] Status para ${endpoint}:`, response.status)
        
        if (response.ok) {
          workingEndpoint = endpoint
          console.log(`‚úÖ [SETTINGS] Endpoint funcionando: ${endpoint}`)
          break
        }
      } catch (error) {
        console.log(`‚ùå [SETTINGS] Erro ao testar ${endpoint}:`, error.message)
        continue
      }
    }
    
    if (!workingEndpoint) {
      console.log('‚ùå [SETTINGS] Nenhum endpoint funcionou com esta API Key')
      return res.status(401).json({ 
        error: 'API Key inv√°lida ou sem permiss√µes para nenhum endpoint',
        details: 'Testados: /report, /conversions, /campaigns, /me/settings',
        suggestions: [
          'Verifique se a API Key est√° correta',
          'A API Key pode ter expirado - gere uma nova no RedTrack',
          'Certifique-se de que a API Key tem permiss√µes adequadas',
          'Plano Solo pode ter acesso limitado - tente endpoints b√°sicos primeiro',
          'Considere fazer upgrade para plano Team/Enterprise para API completa'
        ],
        planInfo: {
          current: 'Solo',
          limitations: 'API access limitado',
          recommendation: 'Teste endpoints b√°sicos ou fa√ßa upgrade'
        }
      })
    }

    console.log('üîç [SETTINGS] Status da resposta:', response.status)
    console.log('üîç [SETTINGS] Headers da resposta:', Object.fromEntries(response.headers.entries()))

    if (!response.ok) {
      const errorData = await response.json().catch(() => ({}))
      console.log('‚ùå [SETTINGS] Erro na resposta:', errorData)
      
      // Mensagens de erro mais espec√≠ficas
      let errorMessage = 'API Key inv√°lida ou erro na API do RedTrack'
      let suggestions = []
      
      if (response.status === 401) {
        errorMessage = 'API Key inv√°lida ou expirada'
        suggestions = [
          'Verifique se a API Key est√° correta',
          'A API Key pode ter expirado - gere uma nova no RedTrack',
          'Certifique-se de que a API Key tem permiss√µes adequadas'
        ]
      } else if (response.status === 403) {
        errorMessage = 'Acesso negado - API Key sem permiss√µes'
        suggestions = [
          'Verifique se a API Key tem permiss√µes para acessar os dados',
          'Entre em contato com o administrador da conta RedTrack'
        ]
      } else if (response.status === 429) {
        errorMessage = 'Limite de requisi√ß√µes excedido'
        suggestions = [
          'Aguarde alguns minutos antes de tentar novamente',
          'Verifique o plano da sua conta RedTrack'
        ]
      }
      
      return res.status(response.status).json({ 
        error: errorMessage,
        details: errorData,
        suggestions: suggestions,
        status: response.status
      })
    }

    const data = await response.json()
    console.log('‚úÖ [SETTINGS] Dados recebidos com sucesso do endpoint:', workingEndpoint)
    res.status(200).json({
      ...data,
      workingEndpoint: workingEndpoint,
      message: 'API Key v√°lida! Conectado com sucesso ao RedTrack.'
    })
    
  } catch (error) {
    console.error('‚ùå [SETTINGS] Erro ao conectar com RedTrack:', error)
    console.error('‚ùå [SETTINGS] Tipo do erro:', typeof error)
    console.error('‚ùå [SETTINGS] Mensagem do erro:', error.message)
    res.status(500).json({ 
      error: 'Erro de conex√£o com a API do RedTrack',
      details: error.message 
    })
  }
} 